    public void Contar(){
        int cont = 0;
        for(String o : TelaChat.Onlines){
            if(!taContador.getText().contains(o)){
                taContador.append(o);
                cont++;
            }
        }
        lbContador.setText(""+cont);
        
        if (cont > 1){            
        if(cont > 10)
            lbContador.setForeground(Color.GREEN);
        }else{
            lbContador.setForeground(Color.RED);
        }
    }

